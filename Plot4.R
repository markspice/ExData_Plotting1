df <- read.csv("household_power_consumption.txt", sep = ";", header = TRUE, na.strings = "?")
df$Date <- as.Date(df$Date, "%d/%m/%Y")
df <- subset(df, Date >= as.Date("2007-02-01") & Date <= as.Date("2007-02-02"))
df$Time <- as.POSIXct(paste(df$Date, df$Time), format="%Y-%m-%d %H:%M:%S")
par(mfrow = c(2,2))
plot(df$Time, df$Global_active_power, type = "l", xlab = NA, ylab = "Global Active Power")
plot(df$Time, df$Voltage, type = "l", xlab = "datetime", ylab = "Voltage")
plot(df$Time, df$Sub_metering_1, type = "l", col = "black", xlab = NA, ylab = "Energy sub metering")
points(df$Time, df$Sub_metering_2, type = "l", col = "red")
points(df$Time, df$Sub_metering_3, type = "l", col = "blue")
legend("topright", bty = "n", lty = 1, col = c("black", "blue", "red"), legend = c("Sub_metering_1", "Sub_metering_2", "Sub_metering_3"), pt.cex = 1, cex = 0.75)
plot(df$Time, df$Global_reactive_power, type = "l", xlab = "datetime", ylab = "Global_reactive_power")
dev.copy(png,"plot4.png")
dev.off()
dev.off()
rm(df)